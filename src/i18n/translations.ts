export type TranslationKey = keyof typeof translations.ru
export type Language = keyof typeof translations

export function getText(key: TranslationKey, language: Language): any {
  return translations[language]?.[key] || translations.ru[key] || key
}

// Функция для склонения слов в зависимости от числа
export function getPluralForm(count: number, language: 'ru' | 'tk', word: { zero: string; one: string; few: string; many: string }) {
  if (language === 'tk') {
    return word.one; // В туркменском языке нет склонений
  }
  
  // Русские правила склонения
  if (count === 0) return word.zero;
  if (count === 1) return word.one;
  if (count >= 2 && count <= 4) return word.few;
  return word.many;
}
